"""
Given an integer array, bits, that represents a binary sequence
(i.e. it only contains zeroes and ones), return the length of the longest contiguous
sequence of bits that contains the same number of zeroes and ones.

Ex: Given the following bits…

bits = [1, 0, 1, 1, 0, 0], return 6 (the entire sequence has the 3 zeroes and 3 ones).
Ex: Given the following bits…

bits = [1, 1], return 0.
"""


class Solution:
    def findMaxLength(self, bits: list[int]) -> int:
        count = 0
        max_length = 0
        table = {0: 0}
        for index, num in enumerate(bits, 1):
            if num == 0:
                count -= 1
            else:
                count += 1

            if count in table:
                max_length = max(max_length, index - table[count])
            else:
                table[count] = index

        return max_length


